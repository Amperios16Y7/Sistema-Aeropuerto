CREATE DATABASE aero;

USE aero;

CREATE TABLE aeropuertos (
  id int NOT NULL AUTO_INCREMENT,
  codigo_iata char(3) NOT NULL,
  codigo_icao char(4) DEFAULT NULL,
  nombre varchar(100) NOT NULL,
  ciudad varchar(50) NOT NULL,
  pais varchar(50) NOT NULL,
  pistas_Aterrizaje int DEFAULT NULL,
  PRIMARY KEY (id),
  UNIQUE KEY codigo_iata (codigo_iata),
  UNIQUE KEY codigo_icao (codigo_icao)
);

CREATE TABLE empleados (
  id int NOT NULL AUTO_INCREMENT,
  RFC varchar(50) NOT NULL,
  nombre varchar(50) NOT NULL,
  apellido_paterno varchar(50) NOT NULL,
  apellido_Materno varchar(50) NOT NULL,
  fecha_nacimiento date DEFAULT NULL,
  cargo varchar(50) NOT NULL,
  salario decimal(10,2) DEFAULT NULL,
  PRIMARY KEY (id),
  UNIQUE KEY RFC (RFC)
);

CREATE TABLE pasajeros (
  id int NOT NULL AUTO_INCREMENT,
  nombre varchar(50) NOT NULL,
  apellido varchar(50) DEFAULT NULL,
  fecha_nacimiento date DEFAULT NULL,
  nacionalidad varchar(50) DEFAULT NULL,
  telefono varchar(20) DEFAULT NULL,
  email varchar(100) DEFAULT NULL,
  pasaporte varchar(20) DEFAULT NULL,
  numero_vuelo varchar(50) DEFAULT NULL,
  PRIMARY KEY (id)
);

CREATE TABLE roles (
  idRol int NOT NULL AUTO_INCREMENT,
  nombreRol varchar(50) NOT NULL,
  PRIMARY KEY (idRol)
);

CREATE TABLE usuarios (
  id int NOT NULL AUTO_INCREMENT,
  nombre varchar(100) NOT NULL,
  apellido varchar(100) NOT NULL,
  correo varchar(100) NOT NULL,
  contraseña varchar(100) NOT NULL,
  rol enum('Administrador','Empleado','Invitado','Supervisor') NOT NULL,
  PRIMARY KEY (id)
);

CREATE TABLE vuelos (
  id INT NOT NULL AUTO_INCREMENT,
  numero_vuelo VARCHAR(10) NOT NULL,
  aerolinea VARCHAR(50) NOT NULL,
  fecha_salida DATETIME NOT NULL,
  fecha_llegada DATETIME NOT NULL,
  estado VARCHAR(20) DEFAULT 'Programado',
  puerta_embarque VARCHAR(5) DEFAULT NULL,
  terminal VARCHAR(5) DEFAULT NULL,
  precio_Boleto INT DEFAULT NULL,
  asientosDisponibles INT DEFAULT NULL,
  destino VARCHAR(50) DEFAULT NULL,
  PRIMARY KEY (id)
);

